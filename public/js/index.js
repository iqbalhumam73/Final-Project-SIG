function toHere(lat, Lng){
    var latLng = L.latLng(Lng, lat);
    control.spliceWaypoints(control.getWaypoints().length - 1, 1, latLng);
}

function fromHere(lat, Lng){
    var latLng = L.latLng(Lng, lat);
    control.spliceWaypoints(0, 1, latLng);;
}